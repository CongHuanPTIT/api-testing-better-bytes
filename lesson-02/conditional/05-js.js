let mode = 'dark'

// Case 1: if - else
if (mode === "dark") {
  console.log("Chế độ tối đã được bật!");
} else if (mode === "light") {
  console.log("Chế độ sáng đã được bật!");
}

// Case 2: switch - case
switch (mode) {
  case "dark":
    console.log("Chế độ tối đã được bật!");
    break;
  case "light":
    console.log("Chế độ sáng đã được bật!");
    break;
}