let kpop = 'bts'

// Case 1: if - else
if (kpop === "bts") {
  console.log("Nghe ngay 'Dynamite'!");
} else if (kpop === "blackpink") {
  console.log("Thử 'How You Like That' nào!");
} else if (kpop === "exo") {
  console.log("Đừng bỏ lỡ 'Love Shot'!");
} else if (kpop === "twice") {
  console.log("Bật ngay 'The Feels' nào!");
}

// Case 2: switch - case
switch (kpop) {
  case "bts":
    console.log("Nghe ngay 'Dynamite'!");
    break;
  case "blackpink":
    console.log("Thử 'How You Like That' nào!");
    break;
  case "exo":
    console.log("Đừng bỏ lỡ 'Love Shot'!");
    break;
  case "twice":
    console.log("Bật ngay 'The Feels' nào!");
    break;
}